function upgradeOutfitIdeas() {
  Object.assign(outfitIdeas, realisticOutfitIdeas);
  addExtraStylingPieces();
}

function addExtraStylingPieces() {
  Object.entries(outfitIdeas).forEach(([mood, occasionGroup]) => {
    Object.entries(occasionGroup).forEach(([occasion, look]) => {
      [moodStylingExtras[mood], occasionStylingExtras[occasion]].forEach((item) => {
        if (item && !look.products.some((product) => product.name === item.name)) {
          look.products.push({ ...item });
        }
      });
    });
  });
}

