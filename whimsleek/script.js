function wireGenerator() {
  const form = document.querySelector("#generator-form");
  if (!form) return;

  const defaultMood = document.querySelector("#style-mood")?.value || "cyber";
  const defaultOccasion = document.querySelector("#occasion")?.value || "campus";
  renderLook(defaultMood, defaultOccasion);

  form.addEventListener("submit", (event) => {
    event.preventDefault();
    const styleMood = document.querySelector("#style-mood")?.value || "cyber";
    const occasion = document.querySelector("#occasion")?.value || "campus";
    renderLook(styleMood, occasion);
  });

  document.querySelector("#shop-look-button")?.addEventListener("click", () => {
    const shopAnchor = document.querySelector("#featured-shop");
    const destination = document.querySelector("#shop-look-button")?.getAttribute("data-shop-destination") || "shop.html";
    if (shopAnchor) {
      shopAnchor.scrollIntoView({ behavior: "smooth", block: "start" });
      return;
    }
    window.location.href = destination;
  });

  document.querySelector("#add-look-button")?.addEventListener("click", () => {
    const savedLook = readSavedLook();
    if (!savedLook) return;
    incrementBagCount(savedLook.products.length);
    announceStatus(`${savedLook.title} added to cart.`);
    alert(`Added the full ${savedLook.title} look to your cart.`);
  });
}

function renderLook(styleMood, occasion) {
  const moodCollection = outfitIdeas[styleMood] || Object.values(outfitIdeas)[0];
  const look = moodCollection?.[occasion] || Object.values(moodCollection || {})[0];
  if (!look) return;

  setText("#look-title", look.title);
  setText("#look-description", look.description);
  setText("#look-vibe", look.vibe);
  setText("#look-top", findProductSummary(look.products, ["Top", "Layer"]) || "");
  setText("#look-bottom", findProductSummary(look.products, ["Bottom"]) || "");
  setText("#look-shoes", findProductSummary(look.products, ["Shoes"]) || "");
  setText("#look-extra", `${findProductSummary(look.products, ["Jewelry", "Bag", "Accessory"]) || "Accessories selected"} - ${look.products.length} shoppable pieces ready now`);

  const total = look.products.reduce((sum, product) => sum + product.price, 0);
  setText("#look-total", `$${total}`);
  setHTML("#look-products", look.products.map(createLookCardMarkup).join(""));
  writeSavedLook({ ...look, mood: styleMood, occasion, total });
}

function findProductSummary(products, categories) {
  const product = products.find((item) => categories.includes(item.category));
  return product ? `${product.category}: ${product.name}` : "";
}

