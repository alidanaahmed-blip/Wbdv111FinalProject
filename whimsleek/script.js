function applyWhimsleekPhotoProducts() {
  let productIndex = 0;
  Object.values(outfitIdeas).forEach((styleGroup) => {
    Object.values(styleGroup).forEach((look) => {
      look.products = look.products.map(() => ({ ...whimsleekPhotoProducts[productIndex++ % whimsleekPhotoProducts.length] }));
    });
  });

  Object.assign(moodStylingExtras.cyber, whimsleekPhotoProducts[5]);
  Object.assign(moodStylingExtras.pastel, whimsleekPhotoProducts[0]);
  Object.assign(moodStylingExtras.monochrome, whimsleekPhotoProducts[4]);
  Object.assign(occasionStylingExtras.campus, whimsleekPhotoProducts[6]);
  Object.assign(occasionStylingExtras.nightlife, whimsleekPhotoProducts[5]);
  Object.assign(occasionStylingExtras.weekend, whimsleekPhotoProducts[3]);

  if (typeof expandedCatalogProducts !== "undefined") {
    expandedCatalogProducts.forEach((product, index) => Object.assign(product, whimsleekPhotoProducts[index % whimsleekPhotoProducts.length]));
  }
}

