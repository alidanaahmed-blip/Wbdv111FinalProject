function wireAuthModal() {
  const modal = document.querySelector("#auth-modal");
  const openButtons = document.querySelectorAll("[data-open-auth]");
  const closeButton = document.querySelector("#close-auth");
  const checkbox = document.querySelector("#agree-terms");
  const continueButton = document.querySelector("#continue-auth");
  const termsStep = document.querySelector("#terms-step");
  const formStep = document.querySelector("#form-step");
  const tabButtons = document.querySelectorAll("[data-auth-tab]");
  const loginForm = document.querySelector("#login-form");
  const signupForm = document.querySelector("#signup-form");
  const roleQuickButtons = document.querySelectorAll("[data-role-quick]");
  const roleQuickLinks = modal?.querySelectorAll(".portal-links a[href]") || [];
  const demoButtons = modal?.querySelectorAll("[data-demo-role]") || [];

  if (!modal || !loginForm || !signupForm) return;

  let requestedMode = "login";
  let pendingDestination = "";

  openButtons.forEach((button) => {
    button.addEventListener("click", () => {
      requestedMode = button.getAttribute("data-open-auth") || "login";
      resetAuthModal();
      modal.hidden = false;
      document.body.style.overflow = "hidden";
    });
  });

  checkbox?.addEventListener("change", () => {
    if (continueButton) continueButton.disabled = !checkbox.checked;
  });

  continueButton?.addEventListener("click", () => {
    if (!pendingDestination) return;
    closeAuthModal(modal);
    window.location.href = pendingDestination;
  });

  tabButtons.forEach((button) => {
    button.addEventListener("click", () => {
      const mode = button.getAttribute("data-auth-tab") || "login";
      switchAuthTab(mode, tabButtons, loginForm, signupForm);
    });
  });

  roleQuickButtons.forEach((button) => {
    button.addEventListener("click", () => {
      const role = button.getAttribute("data-role-quick") || "user";
      applyRoleSelection(role);
    });
  });

  roleQuickLinks.forEach((link) => {
    link.addEventListener("click", (event) => {
      const role = getRoleFromDestination(link.getAttribute("href"));
      if (!role) return;
      event.preventDefault();
      applyRoleSelection(role);
    });
  });

  demoButtons.forEach((button) => {
    button.addEventListener("click", () => {
      const role = button.getAttribute("data-demo-role") || "user";
      autofillDemoCredentials(loginForm, role);
      applyRoleSelection(role);
      switchAuthTab("login", tabButtons, loginForm, signupForm);
    });
  });

  modal.querySelectorAll("[data-role-select]").forEach((select) => {
    select.addEventListener("change", () => {
      syncRoleQuickState(select.value, roleQuickButtons, roleQuickLinks);
      syncDemoState(select.value, demoButtons);
    });
  });

  [loginForm, signupForm].forEach((form) => {
    form.addEventListener("submit", (event) => {
      event.preventDefault();
      if (!form.checkValidity()) {
        form.reportValidity();
        return;
      }
      const role = form.querySelector("[data-role-select]")?.value || "user";
      pendingDestination = getRoleDestination(role);
      showAgreementStep();
    });
  });

  closeButton?.addEventListener("click", () => closeAuthModal(modal));

  modal.addEventListener("click", (event) => {
    if (event.target === modal) closeAuthModal(modal);
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape" && !modal.hidden) {
      closeAuthModal(modal);
    }
  });

  function resetAuthModal() {
    pendingDestination = "";
    if (checkbox) checkbox.checked = false;
    if (continueButton) continueButton.disabled = true;
    if (termsStep) termsStep.classList.remove("auth-step-active");
    if (formStep) formStep.classList.add("auth-step-active");
    applyRoleSelection("user");
    switchAuthTab(requestedMode, tabButtons, loginForm, signupForm);
  }

  function showAgreementStep() {
    if (checkbox) checkbox.checked = false;
    if (continueButton) continueButton.disabled = true;
    if (formStep) formStep.classList.remove("auth-step-active");
    if (termsStep) termsStep.classList.add("auth-step-active");
  }

  function applyRoleSelection(role) {
    modal.querySelectorAll("[data-role-select]").forEach((select) => {
      select.value = role;
    });
    syncRoleQuickState(role, roleQuickButtons, roleQuickLinks);
    syncDemoState(role, demoButtons);
  }
}

function autofillDemoCredentials(loginForm, role) {
  const username = role === "super-admin" ? "superadmin" : role;
  const identityInput = loginForm.querySelector('input[type="text"], input[type="email"]');
  const passwordInput = loginForm.querySelector('input[type="password"]');
  if (identityInput) identityInput.value = username;
  if (passwordInput) passwordInput.value = username;
}

function getRoleDestination(role) {
  if (role === "admin") return "admin.html";
  if (role === "super-admin") return "super-admin.html";
  return "dashboard.html";
}

function switchAuthTab(mode, tabButtons, loginForm, signupForm) {
  tabButtons.forEach((button) => {
    button.classList.toggle("tab-active", button.getAttribute("data-auth-tab") === mode);
  });
  const showLogin = mode === "login";
  loginForm.classList.toggle("auth-form-active", showLogin);
  signupForm.classList.toggle("auth-form-active", !showLogin);
}

function closeAuthModal(modal) {
  modal.hidden = true;
  document.body.style.overflow = "";
}

function getCurrentFileName() {
  return normalizeFileName(window.location.pathname.split("/").pop()) || "index.html";
}

function normalizeFileName(value) {
  return (value || "").split("#")[0].split("?")[0].trim();
}

function readBagCount() {
  const storedValue = window.localStorage.getItem(BAG_STORAGE_KEY);
  const parsedValue = Number.parseInt(storedValue || "0", 10);
  return Number.isFinite(parsedValue) && parsedValue > 0 ? parsedValue : 0;
}

function incrementBagCount(amount) {
  const nextValue = readBagCount() + amount;
  window.localStorage.setItem(BAG_STORAGE_KEY, String(nextValue));
  renderBagCount(nextValue);
}

function renderBagCount(bagCount) {
  document.querySelectorAll("#bag-status, [data-bag-status]").forEach((element) => {
    element.textContent = `Shopping cart: ${bagCount} item${bagCount === 1 ? "" : "s"}`;
  });
}

function writeSavedLook(look) {
  window.localStorage.setItem(LOOK_STORAGE_KEY, JSON.stringify(look));
}

function readSavedLook() {
  try {
    return JSON.parse(window.localStorage.getItem(LOOK_STORAGE_KEY) || "null");
  } catch {
    return null;
  }
}

function pulseButton(button, text) {
  if (!button || button.dataset.busy === "true") return;
  const originalText = button.textContent;
  button.dataset.busy = "true";
  button.textContent = text;
  window.setTimeout(() => {
    button.textContent = originalText;
    button.dataset.busy = "false";
  }, 1200);
}

function announceStatus(message) {
  const liveRegion = document.querySelector("#bag-live-region");
  if (liveRegion) liveRegion.textContent = message;
}

function getRoleFromDestination(destination) {
  const fileName = normalizeFileName(destination);
  if (fileName === "admin.html") return "admin";
  if (fileName === "super-admin.html") return "super-admin";
  if (fileName === "dashboard.html") return "user";
  return "";
}

function syncRoleQuickState(role, roleQuickButtons, roleQuickLinks) {
  roleQuickButtons.forEach((button) => {
    const isActive = button.getAttribute("data-role-quick") === role;
    button.classList.toggle("nav-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  roleQuickLinks.forEach((link) => {
    const isActive = getRoleFromDestination(link.getAttribute("href")) === role;
    link.classList.toggle("nav-active", isActive);
    if (isActive) link.setAttribute("aria-current", "page");
    else link.removeAttribute("aria-current");
  });
}

function syncDemoState(role, demoButtons) {
  demoButtons.forEach((button) => {
    const isActive = button.getAttribute("data-demo-role") === role;
    button.classList.toggle("demo-card-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
}

function setText(selector, value) {
  const element = document.querySelector(selector);
  if (element) element.textContent = value;
}

function setHTML(selector, value) {
  const element = document.querySelector(selector);
  if (element) element.innerHTML = value;
}

function escapeAttribute(value) {
  return String(value).replace(/"/g, "&quot;");
}

function inferCardTitle(button) {
  return button.closest(".product-card, .look-card, .table-card, .highlight-item")?.querySelector("h3, h4, strong")?.textContent?.trim() || "";
}
