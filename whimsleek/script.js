function prepareAuthFormStep(formStep) {
  if (formStep.dataset.sidebarReady === "true") return;
  formStep.dataset.sidebarReady = "true";
  formStep.classList.add("auth-form-step");

  const authTitle = formStep.querySelector("#auth-title");
  if (authTitle) authTitle.textContent = "Welcome back to Whimsleek.";

  const intro = formStep.querySelector(".auth-intro");
  if (intro) {
    intro.innerHTML = `
      <strong>General view first, role access after agreement</strong>
      <p>Use the public site freely before signing in. After you accept the agreement, choose User for shopping, Admin for store management, or Super Admin for platform controls.</p>
    `;

    intro.insertAdjacentHTML("afterend", `
      <div class="general-account-view">
        <span>General view</span>
        <strong>Public visitors can browse before choosing a role.</strong>
        <p>Start with the storefront, generator, shop, about, and contact pages. Role-based dashboards only open after login or registration.</p>
      </div>
    `);
  }

  const portalLinks = formStep.querySelector(".portal-links");
  if (portalLinks) {
    portalLinks.setAttribute("aria-label", "Choose role destination");
    portalLinks.insertAdjacentHTML("beforebegin", `<p class="role-helper">Choose the account destination you want to test.</p>`);
  }

  formStep.insertAdjacentHTML("beforeend", createDemoAccountsMarkup());

  const loginForm = formStep.querySelector("#login-form");
  formStep.querySelectorAll("input").forEach((input) => {
    input.required = true;
  });

  const loginIdentity = loginForm?.querySelector('input[type="email"], input[type="text"]');
  if (loginIdentity) {
    loginIdentity.type = "text";
    loginIdentity.placeholder = "user";
    loginIdentity.setAttribute("autocomplete", "username");
    const label = loginIdentity.closest("label");
    if (label?.firstChild) label.firstChild.textContent = "Username";
  }

  loginForm?.querySelector('input[type="password"]')?.setAttribute("placeholder", "user");
}

